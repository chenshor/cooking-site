<template>
  <b-container>
    <h3>
      {{ title }}:
      <slot></slot>
    </h3>
    <b-row>
      <b-col v-for="r in recipes" :key="r.id">
        <DBPreview class="DBPreview" :recipe="r" />
      </b-col>
    </b-row>
  </b-container>
</template>

<script>
import DBPreview from "./DBPreview";
export default {
  name: "FamilyList",
  components: {
    DBPreview
  },
  props: {
    title: {
      type: String,
      required: true
    }
  },
  data() {
    return {
      recipes: []
    };
  },
  mounted() {
    this.updateRecipes();
  },
  methods: {
    async updateRecipes() {
      try {
        const response = await this.axios.get(
          "https://ass3-2.herokuapp.com/users/FamilyRecipePreview"
        );

        console.log(response);
        const recipes = response.data.userRecipeInfo;

        this.recipes = [];
        this.recipes.push(...recipes);
        // console.log(this.recipes);
      } catch (error) {
        console.log(error);
      }
    }
  }
};
</script>

